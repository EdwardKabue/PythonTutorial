num1 = 3.1425467389
num2 = 10.2903948

#format string with 3 digits
print('num1 {0:.3} is and num2 is {1:.3}'.format(num1, num2))

#format string with 3 decimal places
print('num1 {0:.3f} is and num2 is {1:.3f}'.format(num1, num2))

#format using f-strings
print(f'num1 {num1:.4f} is and num2 is {num2:.4f}')
